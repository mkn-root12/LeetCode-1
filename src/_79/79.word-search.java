    private boolean isExist(char[][] board, int i, int j, String word, int index, boolean[][] visited) {
        final int m = board.length;
        final int n = board[0].length;

        if (index == word.length())
            return true;

        // can not move anywhere
        if (i < 0 || j < 0 || i >= m || j >= n || visited[i][j] || board[i][j] != word.charAt(index))
            return false;

        visited[i][j] = true;

        // left
        boolean left = isExist(board, i, j - 1, word, index + 1, visited);

        // right
        boolean right = isExist(board, i, j + 1, word, index + 1, visited);

        // up
        boolean up = isExist(board, i - 1, j, word, index + 1, visited);

        // down
        boolean down = isExist(board, i + 1, j, word, index + 1, visited);

        if (left || right || up || down)
            return true;

        // reset it, because array is a mutable data structure in Java ...
        visited[i][j] = false;

        return false;
    }

