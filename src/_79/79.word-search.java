    public boolean exist(char[][] board, String word) {
        final int m = board.length;
        if (m == 0)
            return false;
        final int n = board[0].length;

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (word.charAt(0) == board[i][j] && isExist(board, i, j, word, 0, new boolean[m][n]))
                    return true;
            }
        }

        return false;
    }

